    /**
     * 二分法求weight
     *
     * @param weights
     * @param days
     * @return
     */
    public int shipWithinDays(int[] weights, int days) {
        int left = getMaxWeight(weights);
        int right = getSumWeight(weights);
        while (left < right) {
            int mid = left + (right - left) / 2;
            if (canShip(weights, mid, days)) {
                right = mid;
            } else {
                left = mid + 1;
            }
        }
        return left;
    }

    public int getMaxWeight(int[] weights) {
        int max = 0;
        for (int weight : weights) {
            max = Math.max(max, weight);
        }
        return max;
    }

    public int getSumWeight(int[] weights) {
        int sum = 0;
        for (int weight : weights) {
            sum += weight;
        }
        return sum;
    }

    public boolean canShip(int[] weights, int shipWeight, int days) {
        int currWeight = 0;
        for (int weight : weights) {
            if (currWeight + weight <= shipWeight) {
                currWeight += weight;
            } else {
                currWeight = weight;
                days--;
            }
        }
        return --days >= 0;
    }
